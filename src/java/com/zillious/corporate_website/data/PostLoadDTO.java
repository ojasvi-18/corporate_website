package com.zillious.corporate_website.data;

public class PostLoadDTO {
    private long   m_postLoadId;
    private String m_clientIP;
    private String m_appLogs;
    private String m_traceLogs;

    public PostLoadDTO(String clientIP, String appLogs, String traceLogs) {
        m_clientIP = clientIP;
        m_appLogs = appLogs;
        m_traceLogs = traceLogs;
    }

    public long getPostLoadId() {
        return m_postLoadId;
    }

    public String getClientIP() {
        return m_clientIP;
    }

    public String getAppLogs() {
        return m_appLogs;
    }

    public String getTraceLogs() {
        return m_traceLogs;
    }
}
